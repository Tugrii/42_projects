#include <unistd.h>

void	ft_print_comb2(void)
{
	int		a;
	int		b;
	char	num[5];

	a = 0;
	while (a <= 98)
	{
		b = a + 1;
		while (b <= 99)
		{
			num[0] = '0' + a / 10;
			num[1] = '0' + a % 10;
			num[2] = ' ';
			num[3] = '0' + b / 10;
			num[4] = '0' + b % 10;
			write(1, num, 5);
			if (a != 98 || b != 99)
			{
				write(1, ", ", 2);
			}
			b++;
		}
		a++;
	}
}
